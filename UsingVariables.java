public class UsingVariables
{
  public static void main ( String[] args )
  {
    int roomNumber;
    double thatLetter;
    String mySentence;

    roomNumber = 113;
    thatLetter = 2.71828;
    mySentence = "I am learning a bit about Computer Science.";

    System.out.println( "This is room # " + roomNumber);
    System.out.println( "e is close to " + thatLetter);
    System.out.println( mySentence );
  }
}